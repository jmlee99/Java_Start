package loop;

public class While1_1 {
    public static void main(String[] args){
        int count = 0;

        count += 1;
        System.out.println("count = " + count);
        count += 1;
        System.out.println("count = " + count);
        count += 1;
        System.out.println("count = " + count);
        /*이렇게 계속 반복하면 코드의 수정도 복잡하고 코드의 효율성이 떨어진다.*/
    }
}
