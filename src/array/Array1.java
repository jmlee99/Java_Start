package array;

public class Array1 {
    public static void main(String[] args) {
        int student1 = 90;
        int student2 = 80;
        int student3 = 70;
        int student4 = 60;
        int student5 = 50;

        System.out.println("학생1의 점수: " + student1);
        System.out.println("학생2의 점수: " + student2);
        System.out.println("학생3의 점수: " + student3);
        System.out.println("학생4의 점수: " + student4);
        System.out.println("학생5의 점수: " + student5);

        /*학생을 몇 명 더 추가해야 한다면 변수를 선언하는 부분과 점수를 출력하는 부분의
        * 코드도 추가해야한다. 학생을 몇명 더 추가하는 것은 개발자가 코딩으로 해결할 수
        * 있지만, 학생을 수백 명 이상 추가해야 한다면 코드가 상당히 많이 늘어날 것이다.
        * 결국 학생 수가 증가함에 따라 코딩 양이 비례해서 증가하는 것이 문제가 발생한다.*/
    }
}
